// Get ethnic group by ID
export const getEthnicGroup = (id) => ethnicGroups[id];

// Get all ethnic groups in a region
export const getEthnicGroupsByRegion = (regionName) => {
  return Object.values(ethnicGroups).filter(group =>
    group.regions?.includes(regionName)
  );
};

// Get top ethnic groups by population
export const getTopEthnicGroups = (limit = 10) => {
  return Object.values(ethnicGroups)
    .sort((a, b) => b.population - a.population)
    .slice(0, limit);
};

// Search ethnic groups by name
export const searchEthnicGroups = (query) => {
  const lowerQuery = query.toLowerCase();
  return Object.values(ethnicGroups).filter(group =>
    group.name.toLowerCase().includes(lowerQuery)
  );
};
